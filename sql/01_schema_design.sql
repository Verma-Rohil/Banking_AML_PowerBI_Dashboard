-- ============================================================
-- ALERTS TABLE: Generated by anomaly detection
-- ============================================================
CREATE TABLE IF NOT EXISTS alerts (
    alert_id            INT AUTO_INCREMENT PRIMARY KEY,
    transaction_id      INT,
    customer_id         VARCHAR(50),
    alert_type          VARCHAR(50) NOT NULL,                 -- HIGH_AMOUNT, VELOCITY, BALANCE_DRAIN, Z_SCORE, IQR
    severity            VARCHAR(10) NOT NULL,                 -- CRITICAL, HIGH, MEDIUM, LOW
    alert_description   VARCHAR(255),
    detection_method    VARCHAR(50),                          -- SQL, PYTHON_ZSCORE, PYTHON_IQR, PYTHON_ROLLING
    alert_timestamp     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    reviewed            TINYINT DEFAULT 0,                    -- 0 = pending, 1 = reviewed
    false_positive      TINYINT DEFAULT NULL,                 -- NULL = unreviewed, 0 = true alert, 1 = false positive
    reviewer_notes      TEXT,
    FOREIGN KEY (transaction_id) REFERENCES fact_transactions(transaction_id),
    INDEX idx_alert_severity (severity),
    INDEX idx_alert_type (alert_type),
    INDEX idx_alert_reviewed (reviewed)
) ENGINE=InnoDB;

-- ============================================================
-- REPORTING TABLE: Daily monitoring summaries
-- ============================================================
CREATE TABLE IF NOT EXISTS daily_monitoring_report (
    report_id           INT AUTO_INCREMENT PRIMARY KEY,
    report_day          INT NOT NULL,                         -- day number from simulation
    total_txns          INT,
    total_volume        DECIMAL(15,2),
    fraud_count         INT,
    fraud_rate          DECIMAL(8,4),                         -- percentage
    avg_txn_amount      DECIMAL(15,2),
    max_txn_amount      DECIMAL(15,2),
    alert_count         INT,
    high_risk_customers INT,
    report_generated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE KEY uk_report_day (report_day)
) ENGINE=InnoDB;

-- ============================================================
-- INDEXES on fact_transactions for query performance
-- ============================================================
CREATE INDEX idx_ft_customer      ON fact_transactions(customer_id);
CREATE INDEX idx_ft_recipient     ON fact_transactions(recipient_id);
CREATE INDEX idx_ft_type          ON fact_transactions(transaction_type);
CREATE INDEX idx_ft_fraud         ON fact_transactions(is_fraud);
CREATE INDEX idx_ft_day           ON fact_transactions(transaction_day);
CREATE INDEX idx_ft_hour          ON fact_transactions(transaction_hour);
CREATE INDEX idx_ft_amount        ON fact_transactions(amount);
CREATE INDEX idx_ft_step          ON fact_transactions(step);

-- Composite indexes for common query patterns
CREATE INDEX idx_ft_type_fraud    ON fact_transactions(transaction_type, is_fraud);
CREATE INDEX idx_ft_cust_step     ON fact_transactions(customer_id, step);
CREATE INDEX idx_ft_day_type      ON fact_transactions(transaction_day, transaction_type);
