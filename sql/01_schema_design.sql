-- ============================================================
-- 01_schema_design.sql
-- Financial Transaction Monitoring & AML/KYC Compliance System
-- Database: MySQL 8.0+
-- ============================================================

-- Create database
CREATE DATABASE IF NOT EXISTS aml_monitoring;
USE aml_monitoring;

-- ============================================================
-- FACT TABLE: All financial transactions
-- ============================================================
CREATE TABLE IF NOT EXISTS fact_transactions (
    transaction_id          INT AUTO_INCREMENT PRIMARY KEY,
    step                    INT NOT NULL,                    -- 1 step = 1 hour of simulation
    transaction_type        VARCHAR(20) NOT NULL,            -- PAYMENT, TRANSFER, CASH_OUT, DEBIT, CASH_IN
    amount                  DECIMAL(15,2) NOT NULL,
    customer_id             VARCHAR(50) NOT NULL,            -- nameOrig (sender)
    old_balance_orig        DECIMAL(15,2),
    new_balance_orig        DECIMAL(15,2),
    recipient_id            VARCHAR(50) NOT NULL,            -- nameDest (receiver)
    old_balance_dest        DECIMAL(15,2),
    new_balance_dest        DECIMAL(15,2),
    is_fraud                TINYINT DEFAULT 0,               -- 0 = legitimate, 1 = fraudulent
    is_flagged_fraud        TINYINT DEFAULT 0,               -- 0 = not flagged, 1 = flagged by system
    -- Derived columns (populated during ETL)
    transaction_hour        INT,                             -- step % 24 (hour of day)
    transaction_day         INT,                             -- step / 24 (day number)
    balance_change          DECIMAL(15,2),                   -- new_balance_orig - old_balance_orig
    amount_to_balance_ratio DECIMAL(10,4)                    -- amount / old_balance_orig (NULL if old_balance = 0)
) ENGINE=InnoDB;

-- ============================================================
-- DIMENSION TABLE: Customer Profiles (aggregated from fact)
-- ============================================================
CREATE TABLE IF NOT EXISTS dim_customers (
    customer_id         VARCHAR(50) PRIMARY KEY,
    total_transactions  INT DEFAULT 0,
    total_amount        DECIMAL(15,2) DEFAULT 0.00,
    avg_transaction     DECIMAL(15,2) DEFAULT 0.00,
    max_transaction     DECIMAL(15,2) DEFAULT 0.00,
    fraud_count         INT DEFAULT 0,
    first_seen_step     INT,
    last_seen_step      INT,
    risk_tier           VARCHAR(20) DEFAULT 'LOW',           -- CRITICAL, HIGH, MEDIUM, LOW
    risk_score          INT DEFAULT 0                        -- 0-100 numeric score
) ENGINE=InnoDB;

-- ============================================================
-- DIMENSION TABLE: Transaction Types with risk weights
-- ============================================================
CREATE TABLE IF NOT EXISTS dim_transaction_types (
    type_id         INT AUTO_INCREMENT PRIMARY KEY,
    type_name       VARCHAR(20) UNIQUE NOT NULL,
    risk_weight     DECIMAL(3,2) DEFAULT 1.00,               -- multiplier for risk scoring
    description     VARCHAR(100)
) ENGINE=InnoDB;

